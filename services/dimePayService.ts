export const dimePayService = {
    /**
     * Renders the Dime Pay Embedded Widget
     * Note: In production, the signature (JWT) MUST be generated by a backend server.
     */
    renderPaymentWidget: async (props: PaymentWidgetProps) => {

        // Try to get from Supabase first (backend), then fallback to localStorage
        let globalConfig: any = null;
        let companyConfig: any = null;
        
        // Try Supabase if available - get global config from backend
        try {
            const { supabaseService } = await import('./supabaseService');
            globalConfig = await supabaseService.getGlobalConfig();
            
            // Only try to get company-specific settings if companyId is provided AND we're not in signup mode
            // During signup, the company doesn't exist yet, so skip this to avoid errors
            // We can detect signup mode by checking if metadata has signup indicators
            const isSignupFlow = props.metadata?.plan || props.metadata?.company;
            if (props.companyId && !isSignupFlow) {
                // Only fetch company settings for existing companies (e.g., upgrade flow)
                companyConfig = await supabaseService.getPaymentGatewaySettings(props.companyId);
            }
        } catch (e) {
            console.log('Supabase not available, using localStorage');
        }
        
        // Fallback to localStorage if Supabase didn't return config
        if (!globalConfig) {
            globalConfig = storage.getGlobalConfig();
        }
        
        // Priority: company-specific config > global config > demo config
        let config = null;
        if (companyConfig?.dimepay && companyConfig.dimepay.enabled) {
            config = companyConfig.dimepay;
        } else if (globalConfig && globalConfig.dimepay && globalConfig.dimepay.enabled) {
            config = globalConfig.dimepay;
        } else {
            config = DEMO_CONFIG;
        }

        // SAFETY CHECK: If still undefined (should be impossible due to fallback), exit early
        if (!config) {
            props.onError("Payment configuration unavailable.");
            return;
        }

        // Get the active environment credentials
        const activeEnv = config.environment || 'sandbox';
        const activeCredentials = activeEnv === 'production' ? config.production : config.sandbox;

        // Validate credentials exist
        if (!activeCredentials.apiKey || !activeCredentials.secretKey) {
            console.error(`❌ Missing ${activeEnv} credentials`);
            props.onError(`Payment gateway ${activeEnv} credentials not configured.`);
            return;
        }

        console.log('🔧 DimePay Environment:', activeEnv);
        console.log('🔧 DimePay Domain:', activeCredentials.domain);
        console.log('🔧 DimePay Test Mode:', activeEnv === 'sandbox');
        console.log('🔧 DimePay Credentials:', { 
            hasApiKey: !!activeCredentials.apiKey,
            hasMerchantId: !!activeCredentials.merchantId 
        });

        const recurringFreq = props.frequency === 'annual' ? 'YEARLY' : 'MONTHLY';
        
        // Get fee routing configuration from config
        const passFeesTo = config.passFeesTo || 'MERCHANT';

        // Prepare Payload to send to backend for signing
        const orderId = `ORD-${Date.now()}`;
        
        // DimePay Recurring Payments: Order-based subscription model
        // Docs: https://docs.dimepay.net/%EF%B8%8F-dime-apis/z-recurring-payments
        const payloadData = {
            // Standard order fields
            id: orderId,
            currency: props.currency,
            subtotal: props.amount,
            total: props.amount,
            email: props.email,
            
            // Billing details
            billingPerson: {
                name: props.metadata?.name || 'Guest',
                email: props.email
            },
            
            // Items array (required for DimePay orders)
            items: [
                {
                    id: `PLAN-${props.metadata?.plan || 'subscription'}`.replace(/\s+/g, '-').toUpperCase(),
                    name: props.description,
                    price: props.amount,
                    quantity: 1
                }
            ],
            
            // 🎯 SUBSCRIPTION FIELDS - Enable recurring billing
            is_subscription: true,
            subscription_instructions: {
                recurring_frequency: recurringFreq, // 'MONTHLY' or 'YEARLY'
                billing_cycles: props.billingCycles || 9999 // Effectively unlimited unless specified
            },
            
            // Tokenize card for future recurring charges
            tokenize: true,
            
            // Redirect after payment
            redirectUrl: props.metadata?.redirectUrl || window.location.href,
            checkoutUrl: window.location.href,
            
            // Metadata to track subscription in webhook (includes webhook URL per DimePay agent)
            metadata: {
                company_id: props.companyId,
                user_id: props.metadata?.userId,
                plan_name: props.description,
                plan_type: props.metadata?.planType,
                webhookUrl: `${window.location.origin}/api/dimepay-webhook` // Webhook URL in metadata per DimePay guidance
            },
            
            // Fee routing
            pass_fees_to: passFeesTo === 'CUSTOMER' ? 'CUSTOMER' : 'MERCHANT'
        };

        try {
            // Try to use backend signing API (production-ready)
            let jwt: string;
            
            try {
                console.log("[Secure Mode] Requesting backend signature...");
                const response = await fetch('/api/sign-payment', {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify({
                        payload: payloadData,
                        environment: activeEnv
                    })
                });

                if (response.ok) {
                    const data = await response.json();
                    jwt = data.jwt;
                    console.log("✅ Backend signing successful");
                } else {
                    throw new Error('Backend signing failed');
                }
            } catch (backendError: any) {
                // In local development, API endpoints aren't available - this is expected
                const isLocalDev = window.location.hostname === 'localhost' || window.location.hostname === '127.0.0.1';
                
                // Fallback to client-side signing for sandbox only
                if (activeEnv === 'sandbox') {
                    if (isLocalDev) {
                        console.log("ℹ️ Local dev mode: Using client-side signing (sandbox)");
                    } else {
                        console.warn("⚠️ Backend unavailable, using client-side signing (sandbox only)");
                    }
                    jwt = await signPayloadWithHMAC(payloadData, activeCredentials.secretKey);
                } else {
                    console.error("❌ Backend signing required for production");
                    throw new Error('Secure payment signing unavailable');
                }
            }

            // Wait for mount element to exist in DOM
            const mountElement = document.getElementById(props.mountId);
            if (!mountElement) {
                console.error(`❌ Mount element #${props.mountId} not found in DOM`);
                props.onError("Payment form container not found. Please refresh the page.");
                return;
            }

            // Access the global SDK
            const dimepay = (window as any).dimepay || (window as any).DimePay;

            if (!dimepay) {
                console.error("❌ DimePay SDK Global not found. Make sure the DimePay script is loaded.");
                props.onError("Payment SDK failed to load. Please refresh the page.");
                return;
            }

            console.log("🎯 Initializing DimePay widget in element:", props.mountId);

            try {
                dimepay.initPayment({
                    mountId: props.mountId,
                    total: props.amount,
                    currency: props.currency,
                    test: activeEnv === 'sandbox',
                    order_id: orderId,
                    client_id: activeCredentials.apiKey,
                    origin: window.location.origin,
                    data: jwt, 
                    styles: {
                        primaryColor: '#FFA500', 
                        buttonColor: '#000000',
                        buttonTextColor: '#FFFFFF',
                        backgroundColor: '#FFFFFF'
                    },
                    onSuccess: (data: any) => {
                        console.log("✅ DimePay Success:", data);
                        props.onSuccess(data);
                    },
                    onFailed: (err: any) => {
                        console.error("❌ DimePay Failed:", err);
                        props.onError(err);
                    },
                    onError: (err: any) => {
                        console.error("❌ DimePay Error:", err);
                        props.onError(err);
                    }
                });
                console.log("✅ DimePay initPayment called successfully");
            } catch (initError: any) {
                console.error("❌ Error calling dimepay.initPayment:", initError);
                props.onError(initError.message || "Failed to initialize payment form");
            }

        } catch (e) {
            console.error("Error initializing payment:", e);
            props.onError("Payment initialization failed.");
        }
    }
};

// Sign JWT with HMAC-SHA256 using Web Crypto API
const signPayloadWithHMAC = async (payload: any, secretKey: string): Promise<string> => {
    const header = { alg: "HS256", typ: "JWT" };
    
    const base64url = (source: string) => {
        return btoa(source)
            .replace(/=/g, '')
            .replace(/\+/g, '-')
            .replace(/\//g, '_');
    };
    
    const encodedHeader = base64url(JSON.stringify(header));
    const encodedPayload = base64url(JSON.stringify(payload));
    const signatureInput = `${encodedHeader}.${encodedPayload}`;
    
    // Use Web Crypto API to sign with HMAC-SHA256
    const encoder = new TextEncoder();
    const keyData = encoder.encode(secretKey);
    const dataToSign = encoder.encode(signatureInput);
    
    const cryptoKey = await crypto.subtle.importKey(
        'raw',
        keyData,
        { name: 'HMAC', hash: 'SHA-256' },
        false,
        ['sign']
    );
    
    const signature = await crypto.subtle.sign('HMAC', cryptoKey, dataToSign);
    
    // Convert signature to base64url
    const signatureArray = Array.from(new Uint8Array(signature));
    const signatureBase64 = btoa(String.fromCharCode(...signatureArray))
        .replace(/=/g, '')
        .replace(/\+/g, '-')
        .replace(/\//g, '_');
    
    return `${encodedHeader}.${encodedPayload}.${signatureBase64}`;
};